<template>
    <div>
        <h1>欢迎来到{{school}}，学习{{subject}}</h1>
        <h3>当前求和为：{{sum}}</h3>
        <select v-model.number="n">
            <option value="1">1</option>
            <option value="2">2</option>
            <option value="3">3</option>
        </select>
        <button @click="increment(n)">+</button>
        <button @click="decrement(n)">-</button>
        <button @click="incrementOdd(n)">当前求和为奇数时加</button>
        <button @click="incrementWait(n)">等一会儿加</button>
        <h3>当前放大10倍为:{{bigSum}}</h3>
        <h3 style="color: red;">下面Person组件的人数为：{{personList.length}}</h3>
    </div>
</template>

<script>
    import {mapState,mapGetters,mapMutations,mapActions} from 'vuex'
    export default {
        name:'CountLabel',
        data() {
            return {
                n:1,  //用户选择的加项的值                
            }
        },
        computed:{
            //借助mapState生成计算属性，从state中读取数据。 （对象写法）
            ...mapState({school:'school',subject:'subject',sum:'sum',personList:'personList'}),
            ...mapGetters({bigSum:'bigSum'})
        },
        methods: {
            //借助mapMutations生成对应的方法，方法中会调用commit去联系mutations（对象写法）
            ...mapMutations({increment:'JIA',decrement:'JIAN'}),
            //借助mapActions生成对应的方法，方法中会调用dispatch去联系actions（对象写法）
            ...mapActions({incrementOdd:'jiaOdd',incrementWait:'jiaWait'}),
        },
    }
</script>

